function filter(data, criteria) {
    let employees = JSON.parse(data);
    if (criteria != "all") {
        const [criteriaProp, criteriaValue] = criteria.split("-");
        employees = employees.filter(obj => {
            for (let [key, value] of Object.entries(obj)) {
                if (key == criteriaProp && value == criteriaValue) {
                    return true;
                }
            }
            return false;
        });
    }
    let counter = 0;
    for (employee of employees) {
        console.log(`${counter++}. ${employee["first_name"]} ${employee["last_name"]} - ${employee["email"]}`);
    }

}

